    private void BTCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTCalcularActionPerformed
        // TODO add your handling code here:
        double valorTotal, rendaV;
        String renda = "";
        float porcento;
        int  meses;
        
        try {
            renda = TFRenda.getText();
            renda = renda.replace(',', '.');
        } catch (Exception e) {
            JOptionPane.showInputDialog(e);
        }
            
        
        rendaV = Double.parseDouble(renda);
        porcento = Float.parseFloat(TFPercentual.getText());
        meses = Integer.parseInt(TFMeses.getText());
        
        //Calcula percentual
        porcento = porcento / 100;
        
        
        valorTotal = (rendaV * porcento) * meses;
        
        
        LBLValortotal.setText("R$ " + String.format("%.2f", valorTotal));
    }//GEN-LAST:event_BTCalcularActionPerformed

